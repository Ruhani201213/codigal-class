def add(x,y):
    return x+y
def sub(x,y):
    return x-y
def mul(x,y):
    return x*y
def div(x,y):
    return x/y
num1=int(input("enter number 1:"))
num2=int(input("enter number 2:"))

print("sum:",add(num1,num2))
print("subtraction:",sub(num1,num2))
print("product:",mul(num1,num2))
print("division:",div(num1,num2))